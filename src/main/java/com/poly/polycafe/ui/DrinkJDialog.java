    CategoryDAO catDao = new CategoryDAOImpl();
    DrinkDAO drinkDao = new DrinkDAOImpl();
    DefaultTableModel catModel;
    DefaultTableModel drinkModel;
    
    List<Category> catList = List.of();
    List<Drink> drinkList = List.of();
    
    @Override
    public void fillCatTable() {
        catModel = (DefaultTableModel) tblCategories.getModel();
        catModel.setRowCount(0);
        catList = catDao.findAll();
        for(Category cat: catList) {
            Object[] rowDat = {cat.getName()};
            catModel.addRow(rowDat);
        }
        
    }

    @Override
    public void fillDrinkTableByCat() {
        drinkModel.setRowCount(0);

        for(Drink drink: drinkList) {
            Object[] rowData = {
                drink.getId(),
                drink.getName(),
                drink.getUnitPrice(),
                drink.getDiscount()
            };
            drinkModel.addRow(rowData);
        }

    }
    
    @Override
    public void fillDrinkTable() {
        drinkModel = (DefaultTableModel) tblDrinks.getModel();
        drinkModel.setRowCount(0);
        drinkList = drinkDao.findAll();
        
        for(Drink drink: drinkList) {
            Object[] rowData = {
                drink.getId(),
                drink.getName(),
                drink.getUnitPrice(),
                drink.getDiscount()
            };
            drinkModel.addRow(rowData);
        }
    }

    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillCatTable();
        this.fillDrinkTable();
    }

    
