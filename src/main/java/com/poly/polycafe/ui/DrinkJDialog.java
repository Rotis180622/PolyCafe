    private void tblDrinksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDrinksMouseClicked
        int confirm = JOptionPane.showConfirmDialog(null, "Xác nhận chọn món?");
        if(confirm == JOptionPane.YES_OPTION) {
            QuantityDialog quantityDialog = new QuantityDialog(new javax.swing.JFrame(), true);
            quantityDialog.setVisible(true);
            quantity = quantityDialog.getQuantity();
            drinkItem = drinkList.get(tblDrinks.getSelectedRow());

            BillViewModel billVm = new BillViewModel();
            billVm.setDrink(drinkItem);
            billVm.setQuantity(quantity);
            items.add(billVm);
        }
        
    }//GEN-LAST:event_tblDrinksMouseClicked

